#include "True.hpp"
#include "Errors/OutOfRangePinException.hpp"

///////////////////////////////////////////////////////////////////////////////
// Namespace nts::Specials
///////////////////////////////////////////////////////////////////////////////
namespace nts::Specials
{

///////////////////////////////////////////////////////////////////////////////
True::True(const std::string& name)
    : AComponent(name, 1)
{
    m_pins[0] = Pin(Pin::Type::OUTPUT);
    m_pins[0].setState(Tristate::True);
}

///////////////////////////////////////////////////////////////////////////////
Tristate True::compute(size_t pin)
{
    if (pin >= m_pins.size())
        throw OutOfRangePinException();
    return (Tristate::True);
}

} // namespace nts::Specials
